package extrace.misc.smodel;

import java.io.Serializable;

public class LocXY implements Serializable{
	public double x;
	public double y;
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
}
