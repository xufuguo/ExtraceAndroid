package extrace.misc.smodel;

import java.io.Serializable;

public class NamePair implements Serializable{
	public String A;
	public String B;
	public String getA() {
		return A;
	}
	public void setA(String a) {
		A = a;
	}
	public String getB() {
		return B;
	}
	public void setB(String b) {
		B = b;
	}
	
}
